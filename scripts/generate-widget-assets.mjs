/**
 * Generates the design-token values and inline Lucide SVGs used by the
 * embeddable widget (public/widget.js) and splices them into the marked
 * block there.
 *
 * Why build-time injection: widget.js is a self-contained vanilla-JS file
 * running in a shadow root on third-party pages (Plone). It can't consume
 * React components or Tailwind, but it CAN share the design system's
 * source of truth — the token *values* from
 * `@ki4jlu/design-system/tokens.css` and the icon geometry from the same
 * lucide version the admin app renders. Re-run after upgrading either
 * dependency:  npm run build:widget-assets  (also runs as part of `npm
 * run build`).
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

// ── 1. Design tokens ────────────────────────────────────────────────────────
// Parse the LIGHT theme only: the widget renders on arbitrary public pages
// and stays light regardless of the host page or OS theme.
const tokensCssUrl = import.meta.resolve("@ki4jlu/design-system/tokens.css");
const tokensCss = readFileSync(fileURLToPath(tokensCssUrl), "utf8");
// Strip comments first — they mention [data-theme="dark"] in prose, which
// would otherwise truncate the light-theme slice too early.
const uncommented = tokensCss.replace(/\/\*[\s\S]*?\*\//g, "");
const lightCss = uncommented.split('[data-theme="dark"]')[0];

const declarations = {};
for (const [, name, value] of lightCss.matchAll(/(--[\w-]+)\s*:\s*([^;]+);/g)) {
  declarations[name] = value.trim();
}
const resolve = (value, depth = 0) => {
  if (depth > 10) throw new Error(`var() chain too deep at: ${value}`);
  return value.replace(/var\((--[\w-]+)\)/g, (_, ref) => {
    if (!(ref in declarations)) throw new Error(`Unknown token ${ref}`);
    return resolve(declarations[ref], depth + 1);
  });
};
const token = (name) => {
  if (!(name in declarations)) throw new Error(`Token ${name} missing in tokens.css`);
  return resolve(declarations[name]);
};

// Semantic tokens the widget consumes. Keys become `T.<key>` in widget.js.
const T = {
  surface: token("--color-surface"),
  surfaceContainerLowest: token("--color-surface-container-lowest"),
  surfaceContainerLow: token("--color-surface-container-low"),
  onSurface: token("--color-on-surface"),
  onSurfaceVariant: token("--color-on-surface-variant"),
  outline: token("--color-outline"),
  outlineVariant: token("--color-outline-variant"),
  onPrimary: token("--color-on-primary"),
  success: token("--color-success"),
  shadowOverlay: token("--shadow-overlay"),
};

// ── 2. Inline Lucide SVGs ───────────────────────────────────────────────────
// The admin panel stores Lucide keys in `widget.icon` (see
// src/components/widgetOptions.ts — keep WIDGET_ICONS and this list in sync).
// UI_ICONS are the widget's own controls.
const WIDGET_ICON_FILES = {
  Bot: "bot",
  Languages: "languages",
  LineChart: "chart-line",
  Headset: "headset",
  MessageSquare: "message-square",
  Brain: "brain",
  Sparkles: "sparkles",
  Headphones: "headphones",
  Globe: "globe",
  MessageCircle: "message-circle",
};
const UI_ICON_FILES = { X: "x", Send: "send", ThumbsUp: "thumbs-up", ThumbsDown: "thumbs-down" };

const svgAttrs =
  'xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" ' +
  'stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true"';

async function iconSvg(file) {
  const mod = await import(`lucide-react/dist/esm/icons/${file}.mjs`);
  const node = mod.__iconNode;
  if (!Array.isArray(node)) throw new Error(`No __iconNode in lucide icon "${file}"`);
  const children = node
    .map(([tag, attrs]) => {
      const a = Object.entries(attrs)
        .filter(([k]) => k !== "key")
        .map(([k, v]) => `${k}="${v}"`)
        .join(" ");
      return `<${tag} ${a}/>`;
    })
    .join("");
  return `<svg ${svgAttrs}>${children}</svg>`;
}

const ICONS = {};
for (const [key, file] of Object.entries({ ...WIDGET_ICON_FILES, ...UI_ICON_FILES })) {
  ICONS[key] = await iconSvg(file);
}

// ── 3. Splice into public/widget.js ─────────────────────────────────────────
const widgetPath = new URL("../public/widget.js", import.meta.url);
const source = readFileSync(widgetPath, "utf8");
const START = "/* widget-assets:start */";
const END = "/* widget-assets:end */";
const startIdx = source.indexOf(START);
const endIdx = source.indexOf(END);
if (startIdx === -1 || endIdx === -1 || endIdx < startIdx) {
  throw new Error(`Marker block ${START} … ${END} not found in public/widget.js`);
}

const block = [
  START,
  "  // AUTO-GENERATED by scripts/generate-widget-assets.mjs — do not edit by",
  "  // hand. Values come from @ki4jlu/design-system/tokens.css (light theme)",
  "  // and lucide-react. Regenerate: npm run build:widget-assets",
  `  const T = ${JSON.stringify(T, null, 2).replace(/\n/g, "\n  ")};`,
  `  const ICONS = ${JSON.stringify(ICONS, null, 2).replace(/\n/g, "\n  ")};`,
  "  " + END,
].join("\n");

const next = source.slice(0, startIdx) + block + source.slice(endIdx + END.length);
if (next !== source) {
  writeFileSync(widgetPath, next);
  console.log("public/widget.js: token/icon block regenerated");
} else {
  console.log("public/widget.js: token/icon block already up to date");
}
